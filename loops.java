public class loops{
    public static void main(String[] args){
        // For loop
        for (int i=0; i<=20; i++ ){
            System.out.println(i);
        }
        // While loop
        int j=0;
        while (j<=20){
            j++;
            System.out.println(j);
        }

        // Do while loop
        int k = 0;
        do{
            System.out.println(k);
            k++;
        } while(k<=20);
    }
}